Route::middleware(['auth'])->group(function () {
    Route::post('/products', [ProductController::class, 'store'])->name('products.store');
    Route::get('/products/create', [ProductController::class, 'create'])->name('products.create');
    Route::put('/products/{product}', [ProductController::class, 'update'])->name('products.update');
    Route::delete('/products/{product}', [ProductController::class, 'destroy'])->name('products.destroy');
    Route::get('/products/{product}/edit', [ProductController::class, 'edit'])->name('products.edit');
});

Route::get('/products', [ProductController::class, 'index'])->name('products.index');
Route::get('/paymentSuccess', [ProductController::class, 'payment_success']);
Route::get('/products/{product}', [ProductController::class, 'show'])->name('products.show');
Route::post('/products/checkout', [ProductController::class, 'checkout'])->name('products.checkout');
Route::get('/dashboard', [ProductController::class, 'product_list'])->middleware(['auth', 'verified'])->name('dashboard');

Route::middleware('auth')->group(function () {
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::patch('/profile', [ProfileController::class, 'update'])->name('profile.update');
    Route::delete('/profile', [ProfileController::class, 'destroy'])->name('profile.destroy');
});


require __DIR__.'/auth.php';
